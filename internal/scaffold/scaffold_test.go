func TestFileList_SortedOrder(t *testing.T) {
	t.Parallel()

	files := map[string]string{
		"zebra.txt":   "",
		"alpha.txt":   "",
		"beta.txt":    "",
		"gamma.txt":   "",
		"123.txt":     "",
		"UPPER.txt":   "",
		"lower.txt":  "",
		"with/path.txt": "",
	}

	list := FileList(files)

	// Verify the list is sorted
	for i := 1; i < len(list); i++ {
		if list[i-1] > list[i] {
			t.Fatalf("list not sorted at index %d: %q > %q", i-1, list[i-1], list[i])
		}
	}
}

func TestGenerate_ContentSubstitution(t *testing.T) {
	files, err := Generate(Options{AppName: "my-app", ModulePath: "github.com/example/my-app"})
	if err != nil {
		t.Fatalf("Generate returned error: %v", err)
	}

	// Check app name is used in expected places
	if !strings.Contains(files[".env.sample"], "my-app") {
		t.Fatal(".env.sample should contain app name")
	}
	if !strings.Contains(files["docker-compose.yaml"], "my-app") {
		t.Fatal("docker-compose.yaml should contain app name")
	}
	if !strings.Contains(files["README.md"], "# my-app") {
		t.Fatal("README.md should have app name as title")
	}

	// Check module path is used
	if !strings.Contains(files["go.mod"], "module github.com/example/my-app") {
		t.Fatal("go.mod should have correct module path")
	}
	if !strings.Contains(files["cmd/server/main.go"], "github.com/example/my-app/internal/module") {
		t.Fatal("main.go should import internal module with correct path")
	}
}
