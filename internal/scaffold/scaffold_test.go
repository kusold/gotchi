func TestWrite_OverwritesExisting(t *testing.T) {
	t.Parallel()

	root := t.TempDir()

	// Write initial file
	files1 := map[string]string{"test.txt": "initial"}
	if err := Write(root, files1); err != nil {
		t.Fatalf("first Write returned error: %v", err)
	}

	// Overwrite with new content
	files2 := map[string]string{"test.txt": "updated"}
	if err := Write(root, files2); err != nil {
		t.Fatalf("second Write returned error: %v", err)
	}

	data, err := os.ReadFile(filepath.Join(root, "test.txt"))
	if err != nil {
		t.Fatalf("failed to read file: %v", err)
	}
	if string(data) != "updated" {
		t.Fatalf("file not overwritten: got=%q want=%q", string(data), "updated")
	}
}

func TestWrite_CreatesMultipleDirectoryLevels(t *testing.T) {
	t.Parallel()

	root := t.TempDir()
	files := map[string]string{
		"a/b/c/d/e/file.txt": "deeply nested",
	}

	if err := Write(root, files); err != nil {
		t.Fatalf("Write returned error: %v", err)
	}

	data, err := os.ReadFile(filepath.Join(root, "a/b/c/d/e/file.txt"))
	if err != nil {
		t.Fatalf("failed to read deeply nested file: %v", err)
	}
	if string(data) != "deeply nested" {
		t.Fatalf("unexpected content: got=%q", string(data))
	}
}

func TestFileList(t *testing.T) {
	t.Parallel()

	files := map[string]string{
		"c.txt": "",
		"a.txt": "",
		"b.txt": "",
		"d.txt": "",
	}

	list := FileList(files)

	expected := []string{"a.txt", "b.txt", "c.txt", "d.txt"}
	if len(list) != len(expected) {
		t.Fatalf("unexpected length: got=%d want=%d", len(list), len(expected))
	}

	for i, path := range expected {
		if list[i] != path {
			t.Fatalf("file at index %d: got=%s want=%s", i, list[i], path)
		}
	}
}

